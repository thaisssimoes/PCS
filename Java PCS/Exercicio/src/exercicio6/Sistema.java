    private void cadastrarLivrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarLivrosActionPerformed
    if(disponibilidade.isSelected()){
            listaLivros.addElement(String.format("%s(%s) - %s, %s(Disponível)",titulo.getText(),
                                                                                autor.getText(),
                                                                                genero.getSelectedItem(), 
                                                                                anoLivro.getSelectedItem()));
            
           
        }
    else{
        listaLivros.addElement(String.format("%s(%s) - %s, %s",titulo.getText(),
                                                               autor.getText(),
                                                               genero.getSelectedItem(), 
                                                               anoLivro.getSelectedItem()));
    }
        limparLivrosActionPerformed(evt);
    }//GEN-LAST:event_cadastrarLivrosActionPerformed

